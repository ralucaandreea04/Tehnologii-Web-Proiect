                <h3>Nice to know ...</h3>
                <p>The only televised awards ceremony to exclusively honor
                    actors, the SAG Awards® presents 13 awards in
                    TV and film.
                    Voted on by SAG-AFTRA’s robust and diverse membership of
                    130,000+ performers – the SAG Awards has
                    the largest voting body on the awards circuit.
                    Beloved for its style, simplicity, and genuine warmth, the
                    show has become an industry favorite and
                    one of awards season’s most prized honors since its debut in
                    1995.</p>
                <p>Screen Actors Guild Awards (also known as SAG Awards) are
                    accolades given by the Screen Actors
                    Guild-American Federation of Television and Radio Artists
                    (SAG-AFTRA). The award was founded in 1995
                    to recognize outstanding performances in movie and prime
                    time television. SAG Awards have been one
                    of the major awards events in the Hollywood film industry
                    since then, along with the Golden Globe
                    Awards and the Oscars. SAG awards focus both on individual
                    performances and on the work of the
                    entire ensemble of a drama series and comedy series, and the
                    cast of a motion picture.</p>

                <p>The trophy awarded at the Screen Actors Guild (SAG) Awards is
                    known as "The Actor." It is a
                    statuette depicting a nude male figure holding both a mask
                    of comedy and a mask of tragedy, symbolizing
                    the range of emotions actors express in their performances.
                    The SAG Awards are unique in that they
                    exclusively honor performers, with 13 awards presented in
                    both TV and film categories. These awards are
                    highly regarded within the industry and are voted on by the
                    members of SAG-AFTRA, which has a large and
                    diverse membership. The SAG Awards have been a significant
                    part of the Hollywood awards season since
                    their inception in 1995.</p>
                <p>SAG-AFTRA represents more than 160,000 actors, announcers,
                    broadcaster journalists, dancers, DJs,
                    news writers,
                    news editors, program hosts, puppeteers, recording artists,
                    singers, stunt performers, voiceover
                    artists and other professionals.
                    SAG-AFTRA members are the faces and voices that entertain
                    and inform America and the world.</p>
